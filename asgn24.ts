// equality & inequality
let myName: string = "Farhan";
console.log(myName == "Farhan");
console.log(myName !== "farhan");

// numeric
let myNumber = 10;
console.log(myNumber == 10);
console.log(myNumber !== 10);
console.log(myNumber > 10);
console.log(myNumber < 10);
console.log(myNumber >= 10);
console.log(myNumber <= 10);

// & or or
let num1 = 10;
let num2=5;
console.log(num1 > 9 && num2 < 5);
console.log(num1 > 9 || num2 < 5);

let myArray = ["ali", "ahsan"];
let myString = "Sana";
console.log(Array.isArray(myArray));
console.log(Array.isArray(myString));